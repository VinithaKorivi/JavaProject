package PracticePrograms;

public class IncrementOper {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a++);
		System.out.println(a);
        System.out.println(++a + "  "+--a);
        System.out.println("=============");
        int x=23;
        System.out.println(--x);
        System.out.println(x);
        System.out.println(x--);
        System.out.println(x);
	}

}
